package task1;

public class Door {
    String name;
    String side;
    int width;
    int height;
    boolean withGlass;

    public Door(String doorName, String doorSide, int doorWidth, int doorHeight, boolean doorWithGlass){
        name = doorName;
        side = doorSide;
        width = doorWidth;
        height = doorHeight;
        withGlass = doorWithGlass;
    }


}
